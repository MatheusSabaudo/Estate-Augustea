export function formatDate(dateStr) {
  const date = new Date(dateStr);
  if (isNaN(date.getTime())) return 'Data da definire';
  return date.toLocaleDateString('it-IT', {
    weekday: 'long',
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  });
}

export function formatDateShort(dateStr) {
  const date = new Date(dateStr);
  if (isNaN(date.getTime())) return { day: '--', month: '---' };
  return {
    day: date.toLocaleDateString('it-IT', { day: '2-digit' }),
    month: date.toLocaleDateString('it-IT', { month: 'short' }).toUpperCase(),
  };
}

export function filterEventsByCategory(eventsList, category) {
  if (category === 'Tutti') return eventsList;
  return eventsList.filter((event) => event.category === category);
}

export function getFeaturedEvents(eventsList) {
  return eventsList.filter((event) => event.featured);
}

export function getEventBySlug(eventsList, slug) {
  return eventsList.find((event) => event.slug === slug);
}

export function getLocationBySlug(locationsList, slug) {
  return locationsList.find((location) => location.slug === slug);
}

export function sortEventsByDate(eventsList, ascending = true) {
  return [...eventsList].sort((a, b) => {
    const dateA = new Date(a.date);
    const dateB = new Date(b.date);
    return ascending ? dateA - dateB : dateB - dateA;
  });
}
